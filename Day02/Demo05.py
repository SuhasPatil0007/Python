
num1 = 12
print("num1:", num1)
print("type num1:", type(num1)) # int

num2 = 3.14
print("num2:", num2)
print("type num2:", type(num2)) # float

num3 = 2 + 3j
print("num3:", num3)
print("type num3:", type(num3)) # complex

num4 = True
print("num4:", num4)
print("type num4:", type(num4)) # bool

num5 = "Nilesh"
print("num5:", num5)
print("type num5:", type(num5)) # str

