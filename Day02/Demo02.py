
# comments in python start with # symbol.
#   comments contain explanation of code (understandable)
#   comments never executed by python

# program is set of instructions (statements)

# "single line" python statement - instruction
print("Hello, World!")

# multiple "single line" statements can be written on a single line separated by ;
print("Hi, World!"); print("Bye, World!")

# "multi line" python statements
nums = [11,
            22,
    33,
        44]
print(nums)

# a single line statement can be split on multiple lines for better readability
#   using line continuation character (\)
# it is considered as "multi-line statement"
result = 1 + 2 + 3 + 4 + 5 + 6 + 7 + 8 + 9 \
         + 1 + 2 + 3 + 4 + 5 + 6 + 7 + 8 + 9 \
         +  1 + 2 + 3 + 4 + 5 + 6 + 7 + 8 + 9
